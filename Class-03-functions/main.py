
# functions
def my_sum(num1, num2):
   total = num1 + num2
   return  total

print(my_sum(2, 4))
print(my_sum(3, 8))
print(my_sum(20, 30))



def my_sub(num1, num2):
   total = num1 - num2
   return  total
print(my_sub(200, 30))

def calc_avg(grade1, grade2):
  avg = (grade1 + grade2) / 2
  return avg

print(calc_avg(5.5, 9))
print(calc_avg(7, 8))